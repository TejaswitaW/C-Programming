/*Predefined macros in C*/
#include<stdio.h>
#include<string.h>//header file for string functions
int main()
{
    printf("File:%s\n",__FILE__);
    printf("Date:%s\n",__DATE__);
    printf("Time:%s\n",__TIME__);
    printf("Line:%d\n",__LINE__);
    printf("STDC:%d\n",__STDC__);
    printf("Line:%d\n",__LINE__);
}
